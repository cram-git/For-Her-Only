// 4 ways of declaring variables
// Automatically, var, let, const


// automatic variables automatically declare themselves
// x = 5;
// y = 12.5;
// z = "Hello World";

// let - uses the ley keyword, it cannot be redeclared. It is preferred to be used by default when declaring variables
// example
// let x = 5;
// let y = 12.5;
// let z = "Hello World";

// const - the const keyword is used to declare a constant variable. It cannot be redeclared or reassigned
// example
// const x = 5;
// const y = 12.5;
// const z = "Hello World";

// var - the var keyword is used to declare a variable. It can be redeclared and reassigned
// example
// var x = 5;
// var y = 12.5;
// var z = "Hello World";

